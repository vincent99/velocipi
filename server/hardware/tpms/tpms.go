var table *crc16.Table = crc16.MakeTable(crc16.Params{
	Poly:   uint16(0x8005),
	Init:   uint16(0xc759), // 0xff plus crc of the advertisement prefix 0303a5270308425208ff
	RefIn:  false,
	RefOut: false,
	XorOut: uint16(0x0000),
	Name:   "Weird TPMS CRC-16",
})

func checkCrc(data []byte) bool {
	sum := crc16.Checksum(data[0:len(data)-2], table)
	expect := uint16(uint16(data[len(data)-2])<<8 | uint16(data[len(data)-1]))
	got := uint16(uint16(auchCRCHi[sum>>8])<<8 | uint16(auchCRCLo[sum&0xff]))
	return expect == got
}
